let DiaDaSemana = 'Dia1'

switch (DiaDaSemana) {

        case 'Dia1':
        console.log("O Número escolhido foi 1, Hoje é domingo");
        break;

        case 'Dia2':
        console.log("O Número escolhido foi 2, Hoje é segunda");
        break;

        case 'Dia3':
        console.log("O Número escolhido foi 3, Hoje é terça");
        break;

        case 'Dia4':
        console.log("O Número escolhido foi 4, Hoje é quarta");
        break;

        case 'Dia5':
        console.log("O Número escolhido foi 5, Hoje é quinta");
        break;

        case 'Dia6':
        console.log("O Número escolhido foi 6, Hoje é sexta");
        break;

        case 'Dia7':
        console.log("O Número escolhido foi 7, Hoje é sábado");
        break;

        default: 
        console.log("Nenhum dia foi escolhido");
        break;

}